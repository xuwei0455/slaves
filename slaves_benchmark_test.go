package main

import (
	"sync"
	"testing"
	"time"

	"github.com/panjf2000/ants/v2"
)

const (
	BenchParam = 100

	RunTimes         = 1024 * 100
	BenchWorkersSize = 1024
)

func demoFunc() {
	time.Sleep(time.Duration(BenchParam) * time.Millisecond)
}

func BenchmarkGoroutines(b *testing.B) {
	var wg sync.WaitGroup
	for i := 0; i < b.N; i++ {
		wg.Add(RunTimes)
		for j := 0; j < RunTimes; j++ {
			go func() {
				demoFunc()
				wg.Done()
			}()
		}
		wg.Wait()
	}
}

func BenchmarkSemaphore(b *testing.B) {
	var wg sync.WaitGroup
	sema := make(chan struct{}, BenchWorkersSize)

	for i := 0; i < b.N; i++ {
		wg.Add(RunTimes)
		for j := 0; j < RunTimes; j++ {
			sema <- struct{}{}
			go func() {
				demoFunc()
				<-sema
				wg.Done()
			}()
		}
		wg.Wait()
	}
}

func BenchmarkSlaves(b *testing.B) {
	for i := 0; i < b.N; i++ {
		p := New(WithEvilMaxCount(BenchWorkersSize))

		for j := 0; j < RunTimes; j++ {
			_ = p.Add(func() {
				demoFunc()
			})
		}
		p.Join()
	}
}

func BenchmarkAntsPool(b *testing.B) {
	var wg sync.WaitGroup
	p, _ := ants.NewPool(BenchWorkersSize)
	defer p.Release()

	for i := 0; i < b.N; i++ {
		wg.Add(RunTimes)
		for j := 0; j < RunTimes; j++ {
			_ = p.Submit(func() {
				demoFunc()
				wg.Done()
			})
		}
		wg.Wait()
	}
}
